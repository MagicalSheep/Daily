#define P 80112002
const int MAXN = 5e3 + 10;
const int MAXM = 5e5 + 10;

int n, m, cnt, head[MAXN], ind[MAXN], f[MAXN];
ll ans;

struct edge
{
    int to, next;
} Edge[MAXM];

void addEdge(int u, int v)
{
    Edge[++cnt].to = v;
    Edge[cnt].next = head[u];
    head[u] = cnt;
}

ll dfs(int u)
{
    ll tot = 0;
    if (!head[u])
        return 1;
    if (f[u])
        return f[u];
    for (int e = head[u]; e; e = Edge[e].next)
        tot = (tot + dfs(Edge[e].to)) % P;
    return f[u] = tot;
}

int main()
{
    cin >> n >> m;
    while (m--)
    {
        int A, B;
        cin >> A >> B;
        addEdge(B, A);
        ind[A]++;
    }
    for (int i = 1; i <= n; i++)
        if (!ind[i])
            ans = (ans + dfs(i)) % P;
    cout << ans << endl;
    return 0;
}